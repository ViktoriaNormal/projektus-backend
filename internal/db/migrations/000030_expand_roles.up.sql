-- ======================== 6. Переназначение проектных ролей ========================
-- Сейчас в member_roles участникам 4 сидовых проектов назначены либо
-- «Администратор проекта» (владельцу), либо «Разработчик» (всем прочим).
-- Заменим назначения на вариант, зависящий от должности.

-- Убираем прежние назначения только для сидовых участников.
DELETE FROM member_roles
WHERE member_id IN (
    SELECT m.id FROM members m
    WHERE m.project_id IN (
        'c0000000-0000-0000-0001-000000000000'::uuid,
        'c0000000-0000-0000-0002-000000000000'::uuid,
        'c0000000-0000-0000-0003-000000000000'::uuid,
        'c0000000-0000-0000-0004-000000000000'::uuid
    )
);

-- Назначаем заново: владельцу — Админ проекта; прочим — по должности.
INSERT INTO member_roles (member_id, role_id)
SELECT
    m.id,
    CASE
        -- Владелец проекта всегда Админ проекта
        WHEN p.owner_id = m.user_id
            THEN ('e0000000-0000-0000-' || lpad(split_part(m.project_id::text, '-', 4), 4, '0') || '-000000000001')::uuid

        -- По должности
        WHEN u.position = 'Руководитель проектов'
            THEN ('e0000000-0000-0000-' || lpad(split_part(m.project_id::text, '-', 4), 4, '0') || '-000000000003')::uuid  -- Менеджер проекта
        WHEN u.position = 'Бизнес-аналитик'
            THEN ('e0000000-0000-0000-' || lpad(split_part(m.project_id::text, '-', 4), 4, '0') || '-000000000005')::uuid  -- Аналитик
        WHEN u.position IN ('Инженер по тестированию', 'Старший инженер по тестированию')
            THEN ('e0000000-0000-0000-' || lpad(split_part(m.project_id::text, '-', 4), 4, '0') || '-000000000004')::uuid  -- Тестировщик
        WHEN u.position = 'UX/UI-дизайнер'
            THEN ('e0000000-0000-0000-' || lpad(split_part(m.project_id::text, '-', 4), 4, '0') || '-000000000006')::uuid  -- Наблюдатель
        ELSE
            ('e0000000-0000-0000-' || lpad(split_part(m.project_id::text, '-', 4), 4, '0') || '-000000000002')::uuid       -- Разработчик (дефолт)
    END
FROM members m
JOIN projects p ON p.id = m.project_id
JOIN users    u ON u.id = m.user_id
WHERE m.project_id IN (
    'c0000000-0000-0000-0001-000000000000'::uuid,
    'c0000000-0000-0000-0002-000000000000'::uuid,
    'c0000000-0000-0000-0003-000000000000'::uuid,
    'c0000000-0000-0000-0004-000000000000'::uuid
)
ON CONFLICT DO NOTHING;
