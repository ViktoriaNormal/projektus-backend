-- ======================== 5. Назначение системных ролей сид-пользователям ========================
-- Каждому из 121 пользователя должна быть назначена ровно одна или более системных ролей.
-- Разбиение по должности (position):
--   «Руководитель проектов» (employee001..005) → Менеджер проектов; дополнительно
--     первые двое получают ещё «Менеджер шаблонов» (совмещение).
--   «Бизнес-аналитик» (employee006..010) → Менеджер шаблонов; employee006
--     получает также «Менеджер пользователей» как HR-роль для примера.
--   Все остальные разработчики/тестировщики/DevOps/дизайнеры → Обычный пользователь.
--   admin (b0000000-0000-0000-ffff-000000000001) уже имеет «Администратор системы» из 000026.

INSERT INTO user_roles (user_id, role_id)
SELECT
    ('b0000000-0000-0000-0000-' || lpad(n::text, 12, '0'))::uuid AS user_id,
    CASE
        WHEN n BETWEEN 1 AND 5   THEN 'a0000000-0000-0000-0000-000000000004'::uuid  -- Менеджер проектов
        WHEN n BETWEEN 6 AND 10  THEN 'a0000000-0000-0000-0000-000000000003'::uuid  -- Менеджер шаблонов
        ELSE                          'a0000000-0000-0000-0000-000000000005'::uuid  -- Обычный пользователь
    END AS role_id
FROM generate_series(1, 120) AS n
ON CONFLICT DO NOTHING;

