-- =============================================================================
-- 000005: Убрать дублирование options у Приоритизации и Оценки трудозатрат,
--         убрать options у Статуса задачи
-- =============================================================================

-- ======================== 1. Статус задачи — убрать options ========================
-- Статус определяется колонкой доски, не нужны захардкоженные варианты

UPDATE fields
SET options = NULL
WHERE is_system = true AND name = 'Статус задачи' AND kind = 'board_field';

-- ======================== 2. Приоритизация — убрать options, унифицировать описание ========================
-- Конкретные значения приоритета/класса обслуживания определяются настройкой priority_type доски
-- и справочником priority_type_options

UPDATE fields
SET options = NULL,
    description = 'Тип приоритизации определяется настройками доски'
WHERE is_system = true AND name = 'Приоритизация' AND kind = 'board_field';

-- ======================== 3. Оценка трудозатрат — унифицировать описание ========================
-- Единица измерения определяется настройкой estimation_unit доски

UPDATE fields
SET description = 'Единица измерения определяется настройками доски'
WHERE is_system = true AND name = 'Оценка трудозатрат' AND kind = 'board_field';

-- ======================== 4. Статус проекта — убрать options ========================
-- Статусы проекта — фиксированный справочник, отдаётся из Go-кода

UPDATE fields
SET options = NULL
WHERE is_system = true AND name = 'Статус проекта' AND kind = 'project_param';
