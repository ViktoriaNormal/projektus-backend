-- История статусов для только что закрытых багов (id префикс f4100000)
DO $$
DECLARE
    rec RECORD;
    close_ts TIMESTAMPTZ;
    idx INT := 0;
BEGIN
    FOR rec IN
        SELECT t.id AS task_id
        FROM tasks t
        WHERE t.id IN (
            'e1000000-0000-0000-0001-000000000092',
            'e1000000-0000-0000-0001-000000000094',
            'e1000000-0000-0000-0001-000000000120',
            'e1000000-0000-0000-0001-000000000121',
            'e1000000-0000-0000-0001-000000000122',
            'e1000000-0000-0000-0001-000000000123',
            'e1000000-0000-0000-0001-000000000124',
            'e1000000-0000-0000-0001-000000000125',
            'e1000000-0000-0000-0001-000000000128'
        )
    LOOP
        idx := idx + 1;
        close_ts := TIMESTAMPTZ '2026-05-12 09:00:00+03'
            + make_interval(days => (idx % 9)::int)
            + make_interval(hours => (10 + (idx % 6))::int);

        UPDATE task_status_history
        SET left_at = close_ts
        WHERE task_id = rec.task_id
          AND left_at IS NULL;

        INSERT INTO task_status_history (id, task_id, column_id, entered_at, left_at)
        VALUES (
            ('f4100000-0000-0000-0001-' || lpad(idx::text, 12, '0'))::uuid,
            rec.task_id,
            'd0010000-0000-0000-0001-000000000014'::uuid,
            close_ts,
            NULL
        )
        ON CONFLICT (id) DO NOTHING;
    END LOOP;
END $$;

