-- Откат миграции 039: удаляет только баги MOBAPP-96 … MOBAPP-130 и их историю/теги.

DELETE FROM task_tags
WHERE task_id >= 'e1000000-0000-0000-0001-000000000096'
  AND task_id <= 'e1000000-0000-0000-0001-000000000130';

DELETE FROM task_status_history
WHERE task_id >= 'e1000000-0000-0000-0001-000000000096'
  AND task_id <= 'e1000000-0000-0000-0001-000000000130';

DELETE FROM task_dependencies
WHERE (task_id >= 'e1000000-0000-0000-0001-000000000096'
       AND task_id <= 'e1000000-0000-0000-0001-000000000130')
   OR (depends_on_task_id >= 'e1000000-0000-0000-0001-000000000096'
       AND depends_on_task_id <= 'e1000000-0000-0000-0001-000000000130');

DELETE FROM tasks
WHERE board_id = 'd0000000-0000-0000-0001-000000000002'
  AND id >= 'e1000000-0000-0000-0001-000000000096'
  AND id <= 'e1000000-0000-0000-0001-000000000130';
